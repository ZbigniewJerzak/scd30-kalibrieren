input.onButtonPressed(Button.A, function () {
    basic.setLedColor(0xff0000)
    oledssd1306.clearDisplay()
    oledssd1306.writeString("Kalibriere...")
    SCD30.setCalibration400ppm()
    oledssd1306.writeString("Fertig!")
    basic.setLedColor(0x00ff00)
})
input.onButtonPressed(Button.B, function () {
    basic.setLedColor(0xff0000)
    oledssd1306.clearDisplay()
    oledssd1306.writeString("Kalibriere...")
    SCD30.setCalibration400ppm()
    oledssd1306.writeString("Fertig!")
    basic.setLedColor(0x00ff00)
})
oledssd1306.initDisplay()
oledssd1306.turnOn()
oledssd1306.writeString("Starten...")
basic.pause(10000)
basic.forever(function () {
    oledssd1306.clearDisplay()
    oledssd1306.setTextXY(0, 0)
    oledssd1306.writeString("CO2: ")
    oledssd1306.writeNumber(SCD30.readCO2())
    oledssd1306.setTextXY(1, 0)
    oledssd1306.writeString("Tmp: ")
    oledssd1306.writeNumber(SCD30.readTemperature())
    oledssd1306.setTextXY(2, 0)
    oledssd1306.writeString("%Rh: ")
    oledssd1306.writeNumber(SCD30.readHumidity())
    oledssd1306.setTextXY(3, 0)
    oledssd1306.writeString("Cal: ")
    oledssd1306.writeNumber(SCD30.getCalibration())
    oledssd1306.setTextXY(5, 0)
    oledssd1306.writeString("----------------")
    oledssd1306.setTextXY(7, 0)
    oledssd1306.writeString("Ver: ")
    oledssd1306.writeString(SCD30.getVersion())
    basic.pause(10000)
})
